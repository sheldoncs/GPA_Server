	private String clas;
	private String grade;
	private double creditHr;
	private double qualityPoints;
	/**
	 * @return the subjCode
	 */
	public String getSubjCode() {
		return subjCode;
	}
	/**
	 * @param subjCode the subjCode to set
	 */
	public void setSubjCode(String subjCode) {
		this.subjCode = subjCode;
	}
	/**
	 * @return the courseNumb
	 */
	public String getCourseNumb() {
		return courseNumb;
	}
	/**
	 * @param courseNumb the courseNumb to set
	 */
	public void setCourseNumb(String courseNumb) {
		this.courseNumb = courseNumb;
	}
	/**
	 * @return the clas
	 */
